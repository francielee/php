			<p class="title-text">O alicerce da Nutrição no Brasil</p>
			<span>
				</br>
				A Nutrição é uma sociedade sem fins lucrativos que congrega profissionais da área de Nutrição. De caráter técnico, científico, cultural e social, compõe-se de sócios filiados às Associações de Nutrição nos estados.
				A Asbran colabora com o poder público e universidades no sentido de aprimorar a qualidade do ensino de Nutrição e a cada dois anos realiza o Congresso Brasileiro de Nutrição - Conbran. Desenvolve projetos, pesquisas e também funciona como canal de denúncia de fatos que se traduzam em prejuízo ao processo de alimentação e nutrição da população brasileira.	
			</span>
			<p class="title-text" ></br></br>Primeiros Passos</p>
			<span>
			</br>A Nutrição nasceu como Associação Brasileira de Nutricionistas (ABN) em 31 de agosto de 1949, no Rio de Janeiro, e depois passou a se chamar Federação Brasileira das Associações de Nutricionistas – FEBRAN. Sua base de atuação envolve nutricionistas, técnicos e estudantes de nutrição, contando atualmente com uma rede de 9 entidades estaduais filiadas.
			Mesmo tendo a Nutrição iniciado a luta pelo reconhecimento da categoria em meados dos anos 1950, a Lei 5.276, que regulamenta a profissão, só foi sancionada em 1967. A partir da mobilização da Asbran, também nasceram os Conselhos Federal e Regionais de Nutricionistas, em 1976 e, em 1980, o diploma de nutricionista foi regulamentado.
